#include "libft.h"

void	*ft_calloc(size_t num, size_t size)
{
	void	*ptr;

	ptr = (void *)malloc(num * size);
	if (ptr == 0)
		return (NULL);
	ft_bzero(ptr, num * size);
	return (ptr);
}
